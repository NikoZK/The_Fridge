<main id="center" style="text-align: left">
  <h1>About</h1>
  <h3>
    The Fridge is a small full-stack project made for my exam, but the idea behind it is something that could actually
    be useful in real life.
  </h3>
  <h3>
    The goal of the project is to help keep track of food in a shared fridge. Instead of forgetting what is already in
    there, users can log in and manage items in one place. That makes it easier to stay organized and keep a clear
    overview of what is in the fridge, what is in need of buying, and what is already there.
  </h3>
  <h3>
    This project was built with JavaScript, Svelte, and Express. It uses sessions for login, route guards for protected
    pages, a database for storing users and fridge items, and toastr for notifications.
  </h3>
  <h2>What The Fridge can do</h2>
  <ul>
    <li>• Let users create an account and log in</li>
    <li>• Keep certain pages protected for logged-in users only</li>
    <li>• Show fridge items in one shared place</li>
    <li>• Add, update, and delete food items</li>
    <li>• Mark items that are in need, so you know what to buy</li>
    <li>• Use a dinner ideas page for brainstorming what to eat in the coming days</li>
  </ul>
  <h2>Real-life purpose</h2>
  <h3>
    The Fridge is meant to work like a simple shared household or student fridge app. The idea is that people in a
    shared apartment, dorm, or workplace kitchen could use something like this to keep track of what they have, what
    they need to buy, and what ideas they have for dinner.
  </h3>
  <h3>
    So even though this is an exam project, the concept behind it is practical: better overview, easier planning, and a
    simple way to keep fridge and meal ideas in one place.
  </h3>
  <h3>A true lightweight FMS (Fridge Management System)</h3>
